package com.drkrishi.rlt.model;

public class MasterStressModel {
	private Integer stressId;
	private String stressName;
	private Integer stressTypeId;
	private String stressTypeName;
	
	public Integer getStressId() {
		return stressId;
	}
	public void setStressId(Integer stressId) {
		this.stressId = stressId;
	}
	public String getStressName() {
		return stressName;
	}
	public void setStressName(String stressName) {
		this.stressName = stressName;
	}
	public Integer getStressTypeId() {
		return stressTypeId;
	}
	public void setStressTypeId(Integer stressTypeId) {
		this.stressTypeId = stressTypeId;
	}
	public String getStressTypeName() {
		return stressTypeName;
	}
	public void setStressTypeName(String stressTypeName) {
		this.stressTypeName = stressTypeName;
	}
	
}
