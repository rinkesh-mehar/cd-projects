package in.cropdata.cdtmasterdata.dto;

public class favourableSpecificationDto {

	private Float specificationAverage;

	private Float specificationLower;

	private Float specificationUpper;

	public Float getSpecificationAverage() {
		return specificationAverage;
	}

	public void setSpecificationAverage(Float specificationAverage) {
		this.specificationAverage = specificationAverage;
	}

	public Float getSpecificationLower() {
		return specificationLower;
	}

	public void setSpecificationLower(Float specificationLower) {
		this.specificationLower = specificationLower;
	}

	public Float getSpecificationUpper() {
		return specificationUpper;
	}

	public void setSpecificationUpper(Float specificationUpper) {
		this.specificationUpper = specificationUpper;
	}

}
