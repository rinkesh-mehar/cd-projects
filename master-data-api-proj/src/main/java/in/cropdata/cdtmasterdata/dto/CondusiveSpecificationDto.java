package in.cropdata.cdtmasterdata.dto;

public class CondusiveSpecificationDto {

	private String specificationAverage;

	private String specificationLower;

	private String specificationUpper;

	public String getSpecificationAverage() {
		return specificationAverage;
	}

	public void setSpecificationAverage(String specificationAverage) {
		this.specificationAverage = specificationAverage;
	}

	public String getSpecificationLower() {
		return specificationLower;
	}

	public void setSpecificationLower(String specificationLower) {
		this.specificationLower = specificationLower;
	}

	public String getSpecificationUpper() {
		return specificationUpper;
	}

	public void setSpecificationUpper(String specificationUpper) {
		this.specificationUpper = specificationUpper;
	}

}
